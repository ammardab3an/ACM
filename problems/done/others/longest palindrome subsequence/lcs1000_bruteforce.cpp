int go(int l, int r){
	
	if(l >r){
		return 0;
	}
	if(l==r){
		return val[str[l]-'a'];
	}
	
	int &ret = mem[l][r];
	if(vis[l][r]==vid) return ret;
	vis[l][r] = vid;
	
	if(str[l]==str[r]){
		return ret = 2*val[str[l]-'a'] + go(l+1, r-1);
	}
	
	int st_path = go(l+1, r);
	int nd_path = go(l, r-1);
	
	return ret = max(st_path, nd_path);
}

