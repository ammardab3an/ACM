		swap(vec[0], vec[x-1]);
		
		int p = x;
		while(true){
			int g = spf[n/p];
			if(g==1 || p*g >= n) break;
			// cout << p << ' ' << g << endl;
			swap(vec[p-1], vec[p*g-1]);
			p *= g;
		}
		
