long double go(long double a, long double b){
    
    long double ret = 1.0;
    
    if(a){
        long double na = min(a, v);    
        ret += a * go(a-na, (b>acc) * (b+na/2));
    }
    
    if(b){
        long double nb = min(b, v);
        ret += b * go((a>acc) * (a+nb/2.0), b-nb);
    }
    
    return ret;
}

