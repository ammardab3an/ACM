int go(int l, int r){
	
	if(l+1==r){
		return str[l] != str[r];	
	}
	
	int &ret = mem[l][r];
	if(vis[l][r]==vid) return ret;
	vis[l][r] = vid;
	
	int st_path = ((str[l] < str[l+1]) || go(l+2, r)) && ((str[l] < str[r]) || go(l+1, r-1));
	int nd_path = ((str[r] < str[r-1]) || go(l, r-2)) && ((str[r] < str[l]) || go(l+1, r-1));
	
	// cout << l << ' ' << r << ' ' << st_path << ' ' << nd_path << endl;
	
	return ret = st_path || nd_path;
}

